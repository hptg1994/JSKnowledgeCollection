const urlParams = new URLSearchParams(window.location.search);
const index = urlParams.get('index');
let currentItemId = index;
let item = jsonData.groups[currentItemId];
let price = 0;
let showCarousal = false;
let heroImage = item.hero.href;
let carousalImage = item.hero.href;
let currentImageIndex = 0;

if (item.price) {
    price = item.price.regular
} else if (item.priceRange) {
    price = item.priceRange.regular.high;
} else {
    price = 0;
}


function showCarousalModal() {
    showCarousal = !showCarousal;
}

// Get Item Detail
$("#detail-img-hero").attr("src", heroImage);
$("#detail-img-hero").click(
    function () {
        if (showCarousal) {
            $("#carousal-modal").addClass('display-none')
            showCarousalModal();
        } else {
            $("#carousal-modal").removeClass('display-none')
            showCarousalModal();
        }
    }
);
$("#detail-item-info").html("<h1>" + item.name + "</h1><br/><p>$" + price + "</p>");

function changeImage(target, href, index) {
    currentImageIndex = index;
    if (target === 'heroImage') {
        heroImage = href;
        $("#detail-img-hero").attr("src", href);
    } else if (target === "carousalImage") {
        carousalImage = href;
        $("#carousal-image").attr("src", carousalImage);
    }
}


item.images.forEach((element, index) => {
    let htmlContext = "\
        <img src=" + element.href + " class='detail-img-group-singe'} />\
    ";

    let carousal_images = "\
        <img src="+ element.href + " class='carousal-image-group-single carousal-border' />\
        ";
    $(htmlContext).appendTo("#detail-img-group").click((event) => {
        event.stopPropagation()
        changeImage('heroImage', element.href, index);
    });

    $(carousal_images).appendTo("#carousal-image-group").click((event) => {
        event.stopPropagation()
        changeImage('carousalImage', element.href, index);
    });

});

// Carousal Relative

$("#carousal-image").attr("src", carousalImage);
$("#carousal-left").click(
    function (event) {
        event.stopPropagation()
        if (currentImageIndex === 0) {
            currentImageIndex = item.images.length - 1;
        }
        else {
            currentImageIndex -= 1;
        }
        let image = item.images[currentImageIndex].href;
        $("#carousal-image").attr("src", image);
        carousalImage = image
    }
)
$("#carousal-right").click(
    function (event) {
        event.stopPropagation()
        if (currentImageIndex === item.images.length - 1) {
            currentImageIndex = 0;
        }
        else {
            currentImageIndex += 1;
        }
        let image = item.images[currentImageIndex].href;
        $("#carousal-image").attr("src", image);
        carousalImage = image
    }
)
$("#carousal-modal").click(
    function () {
        if (showCarousal) {
            $("#carousal-modal").addClass('display-none')
            showCarousalModal();
        } else {
            $("#carousal-modal").removeClass('display-none')
            showCarousalModal();
        }
    }
);
